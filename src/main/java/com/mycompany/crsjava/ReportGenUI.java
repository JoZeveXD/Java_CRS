    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (currentStudent == null) return;
        
        try {
            currentStudent.getCourses().removeIf(c -> 
                c.getSemester() == sessionSem && c.getYear() == sessionYear
            );
            
            for(int i=0; i < tableModel.getRowCount(); i++) {
                // COLUMN 0 -> CODE
                String code = tableModel.getValueAt(i, 0).toString().trim().toUpperCase();
                
                String name = tableModel.getValueAt(i, 1).toString().trim();
                
                String grade = tableModel.getValueAt(i, 2).toString().trim().toUpperCase();
                
                if(!code.isEmpty()) {
                    int smartCredits = 3; 
                    for(Student s : allStudents) {
                        for(Course c : s.getCourses()) {
                            if(c.getCode().equalsIgnoreCase(code)) {
                                smartCredits = c.getCredits(); 
                                break;
                            }
                        }
                    }
                    
                    currentStudent.addCourse(new Course(code, name, smartCredits, grade, sessionSem, sessionYear));
                }
            }
            
            FileManager.saveGrades(allStudents);
            
            btnSave.setText("Saved");
            btnSave.setForeground(new Color(0, 102, 51));
            btnGenerate.setEnabled(true);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saving: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
    if (currentStudent == null) {
            JOptionPane.showMessageDialog(this, "No student loaded!");
            return;
        }

               List<Course> courses = currentStudent.getFilteredCourses(sessionSem, sessionYear);
        
        if (courses.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Warning: This student has no courses for this semester in memory.\nDid you click 'Save'?");
            return;
        }

        ReportGen gen = new ReportGen();
        String filename = currentStudent.getId() + "_Report_" + sessionSem + "_" + sessionYear + ".pdf";
        
        boolean success = gen.createReport(currentStudent, sessionSem, sessionYear, filename);
        
        if(success) {
            JOptionPane.showMessageDialog(this, "PDF Saved Successfully:\n" + filename);
        } else {
            JOptionPane.showMessageDialog(this, "Error creating PDF. Close the PDF if it is open!");
        }
    }//GEN-LAST:event_btnGenerateActionPerformed

