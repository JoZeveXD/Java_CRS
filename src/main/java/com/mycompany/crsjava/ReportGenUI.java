    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
try {
            String targetID = txtID.getText().trim();
            if(targetID.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a Student ID.");
                return;
            }
            
            sessionSem = Integer.parseInt(txtSem.getText().trim());
            sessionYear = Integer.parseInt(txtYear.getText().trim());

            allStudents = FileManager.loadAllData();
            
            currentStudent = null;
            for(Student s : allStudents) {
                if(s.getId().equalsIgnoreCase(targetID)) {
                    currentStudent = s;
                    break;
                }
            }
            
            tableModel.setRowCount(0);
            
            if(currentStudent != null) {
                lblStudentName.setText("Student: " + currentStudent.getName());
                lblStudentName.setForeground(Color.BLACK);
                
                enableEditor(true);

                boolean foundCourses = false;
                for(Course c : currentStudent.getCourses()) {
                    if (c.getSemester() == sessionSem && c.getYear() == sessionYear) {
                        tableModel.addRow(new Object[] { c.getCode(), c.getName(), c.getLetterGrade() });
                        foundCourses = true;
                    }
                }
                
                if (!foundCourses) {
                    JOptionPane.showMessageDialog(this, 
                        "Student found, but no records for Sem " + sessionSem + " / " + sessionYear + ".\n" +
                        "Table is empty. You can add new results manually.");
                }
                
            } else {
                lblStudentName.setText("Student Not Found");
                lblStudentName.setForeground(Color.RED);
                enableEditor(false);
                JOptionPane.showMessageDialog(this, "Student ID not found in database.");
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers for Semester and Year.");
        } catch (HeadlessException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoadActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       JTextField txtCodeInput = new JTextField();
        JTextField txtNameInput = new JTextField();
        String[] grades = {"A", "A-", "B+", "B", "B-", "C+", "C", "D", "F"};
        JComboBox<String> cmbGradeInput = new JComboBox<>(grades);

        Object[] message = {
            "Course Code (e.g. C204):", txtCodeInput,
            "Course Name (e.g. Algorithms):", txtNameInput,
            "Grade:", cmbGradeInput
        };

        int option = JOptionPane.showConfirmDialog(this, message, "Add New Course Result", JOptionPane.OK_CANCEL_OPTION);

        if (option == JOptionPane.OK_OPTION) {
            String newCode = txtCodeInput.getText().trim();
            String newName = txtNameInput.getText().trim();
            String newGrade = (String) cmbGradeInput.getSelectedItem();

            if (!newCode.isEmpty()) {
                tableModel.addRow(new Object[]{newCode, newName, newGrade});
                
                btnGenerate.setEnabled(false);
                btnSave.setText("SAVE CHANGES *");
                btnSave.setForeground(Color.RED);
            } else {
                JOptionPane.showMessageDialog(this, "Course Code cannot be empty.");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

