    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                        
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword());

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter username and password");
            return;
        }

        User user = um.login(username, password);

        if (user != null) {

        user.setLastLogin(Long.toBinaryString(System.currentTimeMillis()));
        
        um.saveUsers("users.txt");

        JOptionPane.showMessageDialog(this, "Login Successful!");
        
        if (user instanceof AcademicOfficer){
            new AcademicOfficerMenu(um,user).setVisible(true);
        }
        else if (user instanceof CourseAdmin){
            new CourseAdminMenu(um,user).setVisible(true);
        }
 
        this.dispose();
        
        }else{
            JOptionPane.showMessageDialog(this, "Invalid login OR inactive account!");
        }
}

    
